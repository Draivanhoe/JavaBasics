// Definire una funzione che accetti in ingresso 2 interi e ne restituisca la somma.

public class Esercizio05 {
    public static void main(String[] args) {
        System.out.println(sommaDiInteri(12, 3));
    }

    public static int sommaDiInteri (int intero1, int intero2) {
        return intero1 + intero2;
    }
}